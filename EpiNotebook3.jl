md"""
First, we will define the SIR model as a function which is designed to be compatible with the OrdinaryDiffEq package - which is used to simulate differential equations. In the below code block:
* the rate of change of the state variables is defined by du
* the state variables are represented by u
* the parameters by p, and
* time (which we do use here) is given by t.
"""

# ‚ïî‚ïê‚ï° 8f6847d3-6627-432a-ad2e-917d222378d4
function SIRmodel!(du,u,p,t)
    S, I, R = u
    Œ≤, Œ≥ = p
 
    du[1] = -Œ≤*S*I
    du[2] = Œ≤*S*I - Œ≥*I
    du[3] = Œ≥*I
end

# ‚ïî‚ïê‚ï° 69c953e2-c774-450f-9a81-b4499305f115
md"""
Ok! Now we have our model ‚Äî we now need to consider a particular scenario to numerically simulate and write out the corresponding parameters. In the below we specify a simulation over 100 days, where 1 in 10,000 people in the population are initially infected by a disease with transmission rate of Œ≤ = 0.4/day and remains infectious for an average of 10 days i.e., the recovery rate Œ≥ is 1/10 = 0.1/day.
"""

# ‚ïî‚ïê‚ï° b337078e-314c-4401-bbdd-891dc5630292
begin
	#Information on timing:
	#timespan of between 0 and 100 days.
	tspan =(0.0, 100.0)
	#time resolution of simulation output to be recorded every 0.5 days
	tres = 0.5  

	#Information on population:
	#initial infections in population size N of 10,000
	N = 10_000 #pop size
	I_0 = 1/N #initial fraction of infected population
	 
	#initialise population fractions in states S, I, R which should sum to one.
	u0 = [1-I_0, I_0, 0.0] 
	#  1-I_0 + I_0 + 0 = ? 
end

